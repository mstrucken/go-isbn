// Command gen parses RangeMessage.xml and emits ranges.go for the isbn package.
package main

import (
	"encoding/xml"
	"flag"
	"fmt"
	"log"
	"os"
	"strconv"
	"strings"
	"text/template"
)

// ---- XML structures ----

type rangeMessage struct {
	MessageDate string      `xml:"MessageDate"`
	EANPrefixes eanPrefixes `xml:"EAN.UCCPrefixes"`
	RegGroups   regGroups   `xml:"RegistrationGroups"`
}

type eanPrefixes struct {
	Items []eanUCC `xml:"EAN.UCC"`
}

type eanUCC struct {
	Prefix string   `xml:"Prefix"`
	Agency string   `xml:"Agency"`
	Rules  xmlRules `xml:"Rules"`
}

type regGroups struct {
	Items []group `xml:"Group"`
}

type group struct {
	Prefix string   `xml:"Prefix"`
	Agency string   `xml:"Agency"`
	Rules  xmlRules `xml:"Rules"`
}

type xmlRules struct {
	Rules []xmlRule `xml:"Rule"`
}

type xmlRule struct {
	Range  string `xml:"Range"`
	Length int    `xml:"Length"`
}

// ---- Parsed / template data ----

type prefixRangeData struct {
	Lo, Hi uint32
	Length int
}

type groupRangeData struct {
	Lo, Hi uint32
	Length int
}

type prefixData struct {
	Prefix string
	Ranges []prefixRangeData
}

type groupData struct {
	Prefix string
	Agency string
	Ranges []groupRangeData
}

// parseRange parses a "NNNNNNN-NNNNNNN" string into two uint32 values.
func parseRange(r string) (lo, hi uint32, err error) {
	parts := strings.SplitN(r, "-", 2)
	if len(parts) != 2 {
		return 0, 0, fmt.Errorf("invalid range %q", r)
	}
	l, err := strconv.ParseUint(parts[0], 10, 32)
	if err != nil {
		return 0, 0, fmt.Errorf("invalid range lo %q: %w", r, err)
	}
	h, err := strconv.ParseUint(parts[1], 10, 32)
	if err != nil {
		return 0, 0, fmt.Errorf("invalid range hi %q: %w", r, err)
	}
	return uint32(l), uint32(h), nil
}

var tmpl = template.Must(template.New("ranges").Funcs(template.FuncMap{
	"quote": func(s string) string { return strconv.Quote(s) },
}).Parse(`// Code generated by cmd/gen/main.go — DO NOT EDIT.
// Source: RangeMessage.xml, MessageDate: {{.MessageDate}}

package isbn

// prefixRange describes one rule inside an EAN.UCC prefix block.
type prefixRange struct {
	lo, hi uint32 // 7-digit range boundaries (inclusive)
	length int    // registration group element length (0 = undefined)
}

// groupRange describes one rule inside a RegistrationGroup block.
type groupRange struct {
	lo, hi uint32 // range boundaries within the group's digit space
	length int    // registrant element length (0 = undefined)
}

// registrationGroup holds all rules for one group prefix (e.g. "978-0").
type registrationGroup struct {
	prefix string // e.g. "978-0"
	agency string // e.g. "English language"
	ranges []groupRange
}

var gs1Prefixes = map[string][]prefixRange{
{{- range .Prefixes}}
	{{quote .Prefix}}: {
		{{- range .Ranges}}
		{lo: {{.Lo}}, hi: {{.Hi}}, length: {{.Length}}},
		{{- end}}
	},
{{- end}}
}

var registrationGroups = []registrationGroup{
{{- range .Groups}}
	{
		prefix: {{quote .Prefix}},
		agency: {{quote .Agency}},
		ranges: []groupRange{
			{{- range .Ranges}}
			{lo: {{.Lo}}, hi: {{.Hi}}, length: {{.Length}}},
			{{- end}}
		},
	},
{{- end}}
}
`))

func main() {
	inputFlag := flag.String("input", "RangeMessage.xml", "path to RangeMessage.xml (download from https://www.isbn-international.org/range_file_generation)")
	outputFlag := flag.String("output", "ranges.go", "output file path")
	flag.Parse()

	data, err := os.ReadFile(*inputFlag)
	if err != nil {
		log.Fatalf("reading input: %v", err)
	}

	var msg rangeMessage
	if err := xml.Unmarshal(data, &msg); err != nil {
		log.Fatalf("parsing XML: %v", err)
	}

	// Build prefix data
	prefixes := make([]prefixData, 0, len(msg.EANPrefixes.Items))
	for _, ean := range msg.EANPrefixes.Items {
		pd := prefixData{Prefix: ean.Prefix}
		for _, r := range ean.Rules.Rules {
			lo, hi, err := parseRange(r.Range)
			if err != nil {
				log.Fatalf("EAN.UCC prefix %s: %v", ean.Prefix, err)
			}
			pd.Ranges = append(pd.Ranges, prefixRangeData{Lo: lo, Hi: hi, Length: r.Length})
		}
		prefixes = append(prefixes, pd)
	}

	// Build group data
	groups := make([]groupData, 0, len(msg.RegGroups.Items))
	for _, g := range msg.RegGroups.Items {
		gd := groupData{Prefix: g.Prefix, Agency: g.Agency}
		for _, r := range g.Rules.Rules {
			lo, hi, err := parseRange(r.Range)
			if err != nil {
				log.Fatalf("Group %s: %v", g.Prefix, err)
			}
			gd.Ranges = append(gd.Ranges, groupRangeData{Lo: lo, Hi: hi, Length: r.Length})
		}
		groups = append(groups, gd)
	}

	out, err := os.Create(*outputFlag)
	if err != nil {
		log.Fatalf("creating output: %v", err)
	}
	defer out.Close()

	if err := tmpl.Execute(out, map[string]any{
		"MessageDate": msg.MessageDate,
		"Prefixes":    prefixes,
		"Groups":      groups,
	}); err != nil {
		log.Fatalf("rendering template: %v", err)
	}
}
